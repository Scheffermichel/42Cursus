#include <stdio.h>

int	ft_isalnum(char *c)
{
	if ((*c >= 'a' && *c <= 'z') || (*c >= 'A' && *c <= 'Z')
		|| (*c >= '0' && *c <= '9' ))
		return (*c);
	else
		return (0);
}

int	main(void)
{
	char	*c;

	c = "a";
	printf("%d", ft_isalnum(c));
}
