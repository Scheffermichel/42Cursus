#include <stdio.h>

int	ft_strlen(char *s)
{
	int	i;

	i = 0;
	while (*s != '\0')
	{
		i++;
		s++;
	}
	return (i);
}

int	main(void)
{
	char	*s;

	s = "123";
	printf("%d", ft_strlen(s));
}
