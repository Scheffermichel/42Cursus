#include <stdio.h>

int ft_isalpha(char *c)
{
    if((*c >= 'a' && *c <= 'z') || (*c >= 'A' && *c <= 'Z'))
        return (*c);
    else
        return (0);
}

int main()
{
    char *c;
    c = "abcd";

    printf("%d\n", ft_isalpha(c));
    return 0;
}
